masyarakat/pasien: mendapatkan kemudahan akses pelayanan rawat jalan secara daring dan kepastian layanan dan terhindar dari risiko penularan covid-19;
Manajemen RSMS: berupa total quality control pelayanan rumah sakit dan sumber daya lain yang mendukung pelayanan yang menjamin validitas pelayanan dan akuntabilitas;
